static void render_status(void) {
    switch (get_highest_layer(layer_state)) {
        case 1:
            oled_write_P(PSTR("1 2 3"), false);
            break;
        case 2:
            oled_write_P(PSTR("! @ #"), false);
            break;
        default:
            render_logo();
    }
}

bool oled_task_user(void) {
    oled_set_cursor(0,0);
    render_status();
    return false;
}
